// Photo pass 3: fix the Art Institute (logo → building), retry Tower Theatre
// and Yavapai Lodge on Commons with keyword filters, and try Openverse
// (CC-licensed aggregator, incl. Flickr) for the hotels Commons doesn't have.
// Run: node scripts/fetch-photos-pass3.mjs
import { readFileSync, writeFileSync, appendFileSync } from 'node:fs';
import { Buffer } from 'node:buffer';

const UA = 'Route66TripCompanion/1.0 (personal trip app)';
const photosPath = new URL('../src/data/photos.ts', import.meta.url);
const photos = JSON.parse(readFileSync(photosPath, 'utf8').match(/=\s*(\{[\s\S]*\});/)[1]);
const outDir = new URL('../public/img/poi/', import.meta.url);
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));
const credits = [];

async function saveImage(id, url, mime) {
  const img = await fetch(url, { headers: { 'User-Agent': UA } });
  if (!img.ok) throw new Error(`image ${img.status}`);
  const ext = mime === 'image/png' ? 'png' : 'jpg';
  const file = `${id}.${ext}`;
  writeFileSync(new URL(file, outDir), Buffer.from(await img.arrayBuffer()));
  photos[id] = `img/poi/${file}`;
  return file;
}

/** Commons file search; `avoid` = regex of title words that mark wrong shots. */
async function commons(id, query, avoid) {
  const api =
    'https://commons.wikimedia.org/w/api.php?action=query&generator=search' +
    `&gsrsearch=${encodeURIComponent(query)}&gsrnamespace=6&gsrlimit=15` +
    '&prop=imageinfo&iiprop=url|mime&iiurlwidth=640&format=json';
  const data = await (await fetch(api, { headers: { 'User-Agent': UA } })).json();
  const pages = Object.values(data.query?.pages ?? {}).sort((a, b) => a.index - b.index);
  const hit = pages.find(
    (p) =>
      ['image/jpeg', 'image/png'].includes(p.imageinfo?.[0]?.mime) &&
      !(avoid && avoid.test(p.title)),
  );
  if (!hit) throw new Error('no commons result');
  const info = hit.imageinfo[0];
  const file = await saveImage(id, info.thumburl ?? info.url, info.mime);
  credits.push(`- \`${file}\` — [${hit.title}](${info.descriptionurl}) (Wikimedia Commons; see the file page for author & license)`);
  console.log(`ok   ${id}  ←  ${hit.title}`);
}

/** Openverse (CC images from Flickr & co.). */
async function openverse(id, query) {
  const api =
    'https://api.openverse.org/v1/images/?page_size=10&q=' + encodeURIComponent(query);
  const res = await fetch(api, { headers: { 'User-Agent': UA } });
  if (!res.ok) throw new Error(`openverse ${res.status}`);
  const data = await res.json();
  const hit = (data.results ?? []).find((r) => /jpe?g|png/i.test(r.url ?? ''));
  if (!hit) throw new Error('no openverse result');
  const file = await saveImage(id, hit.url, /png$/i.test(hit.url) ? 'image/png' : 'image/jpeg');
  credits.push(`- \`${file}\` — "[${hit.title}](${hit.foreign_landing_url})" by ${hit.creator ?? 'unknown'} (${hit.license.toUpperCase()} via Openverse)`);
  console.log(`ok   ${id}  ←  ${hit.title} [${hit.license}]`);
}

// --- Commons retries with filters -----------------------------------------
const commonsJobs = [
  ['chi-art-institute', 'Art Institute of Chicago building lion entrance', /logo|seal|map|plan/i],
  ['leg3-tower-theatre', 'Tower Theatre Oklahoma City', /performing|concert|interior|Chats/i],
  ['grand-canyon', 'Yavapai Lodge Grand Canyon', /charging|vehicle|EV/i],
];
for (const [id, q, avoid] of commonsJobs) {
  try {
    await commons(id, q, avoid);
  } catch (e) {
    console.log(`skip ${id} (commons): ${e.message}`);
  }
  await sleep(700);
}

// --- Openverse for the hotels Commons doesn't cover ------------------------
const openverseJobs = [
  ['chicago', 'Home2 Suites Chicago River North hotel'],
  ['stlouis', 'Pear Tree Inn St Louis Union Station'],
  ['tulsa', 'La Quinta Inn Owasso'],
  ['amarillo', 'Spark by Hilton Amarillo'],
  ['springdale', 'Hyatt Place Springdale Zion'],
  ['three-rivers', 'Lazy J Ranch Motel Three Rivers'],
  ['los-angeles', 'Park Plaza Lodge Hotel Beverly Los Angeles'],
  ['leg3-tower-theatre', 'Tower Theatre marquee Oklahoma City'],
];
for (const [id, q] of openverseJobs) {
  if (photos[id]) {
    console.log(`already have ${id}`);
    continue;
  }
  try {
    await openverse(id, q);
  } catch (e) {
    console.log(`skip ${id} (openverse): ${e.message}`);
  }
  await sleep(1200);
}

writeFileSync(
  photosPath,
  `// AUTO-GENERATED by scripts/fetch-photos.mjs — do not edit by hand.
// POI/hotel id → bundled photo path (relative to the app base URL).
export const PHOTOS: Record<string, string> = ${JSON.stringify(photos, null, 2)};
`,
);
if (credits.length) appendFileSync(new URL('../PHOTO_CREDITS.md', import.meta.url), credits.join('\n') + '\n');
console.log(`\ntotal photos now: ${Object.keys(photos).length}`);
