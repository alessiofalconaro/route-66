// Second pass for photos: for the ids the Wikipedia lead-image pass could not
// cover (svg logos, missing articles), search Wikimedia Commons directly and
// take the first bitmap result. Merges into src/data/photos.ts + credits.
// Run with: node scripts/fetch-photos-fill.mjs
import { readFileSync, writeFileSync, appendFileSync } from 'node:fs';
import { Buffer } from 'node:buffer';

// id → Commons search query (file namespace). Chosen to be as specific as
// possible so the first hit is the right subject.
const QUERIES = {
  // failed in pass 1
  'la-academy-museum': 'Academy Museum of Motion Pictures building',
  'la-venice-beach': 'Venice Beach boardwalk Los Angeles',
  'las-vegas': 'Caesars Palace Las Vegas hotel exterior',
  'leg2-red-oak': 'Red Oak II Carthage Missouri',
  'leg3-cyrus-avery': 'Cyrus Avery Centennial Plaza Tulsa',
  'leg3-tower-theatre': 'Tower Theatre Oklahoma City',
  'leg4-leaning-tower': 'leaning water tower Groom Texas',
  'leg4-ok66-museum': 'Oklahoma Route 66 Museum Clinton',
  'leg7-yavapai': 'Yavapai Point Grand Canyon',
  'leg9-caesars': 'Caesars Palace Las Vegas facade',
  'zion-weeping-rock': 'Weeping Rock Zion National Park',
  // never mapped in pass 1
  'chi-r66-sign': 'Route 66 begin sign Chicago Adams Street',
  'chi-bulls-store': 'Michael Jordan statue United Center',
  'leg1-lauterbach': 'Lauterbach Giant Springfield Illinois',
  'leg3-tristate': 'tri-state marker Kansas Oklahoma Missouri',
  'leg11-celebration': 'Santa Monica Pier entrance arch sign',
  // hotels (best effort — most motels have no free photo, they just skip)
  chicago: 'Home2 Suites Chicago River North',
  stlouis: 'Pear Tree Inn Union Station St. Louis',
  tulsa: 'La Quinta Inn Owasso Oklahoma',
  amarillo: 'Spark by Hilton Amarillo',
  albuquerque: 'Embassy Suites Albuquerque hotel',
  'grand-canyon': 'Yavapai Lodge Grand Canyon',
  springdale: 'Hyatt Place Springdale Utah',
  'three-rivers': 'Lazy J Ranch Motel Three Rivers California',
  'los-angeles': 'Park Plaza Lodge Hotel Los Angeles',
};

const UA = 'Route66TripCompanion/1.0 (personal trip app; github.com/alessiofalconaro/route-66)';
const outDir = new URL('../public/img/poi/', import.meta.url);
const photosPath = new URL('../src/data/photos.ts', import.meta.url);

// Read the existing map out of the generated TS file (it's just JSON inside).
const photos = JSON.parse(
  readFileSync(photosPath, 'utf8').match(/=\s*(\{[\s\S]*\});/)[1],
);

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

async function fetchRetry(url) {
  for (const wait of [0, 3000, 8000, 20000]) {
    if (wait) await sleep(wait);
    const res = await fetch(url, { headers: { 'User-Agent': UA } });
    if (res.ok) return res;
    if (res.status !== 429 && res.status < 500) return res;
  }
  throw new Error('rate-limited after retries');
}

const credits = [];
for (const [id, query] of Object.entries(QUERIES)) {
  if (photos[id]) {
    console.log(`already have ${id}`);
    continue;
  }
  try {
    const api =
      'https://commons.wikimedia.org/w/api.php?action=query&generator=search' +
      `&gsrsearch=${encodeURIComponent(query)}&gsrnamespace=6&gsrlimit=8` +
      '&prop=imageinfo&iiprop=url|mime&iiurlwidth=640&format=json';
    const res = await fetchRetry(api);
    if (!res.ok) throw new Error(`search ${res.status}`);
    const data = await res.json();
    const pages = Object.values(data.query?.pages ?? {}).sort((a, b) => a.index - b.index);
    const hit = pages.find((p) =>
      ['image/jpeg', 'image/png'].includes(p.imageinfo?.[0]?.mime),
    );
    if (!hit) throw new Error('no bitmap result');
    const info = hit.imageinfo[0];
    const src = info.thumburl ?? info.url;

    const img = await fetchRetry(src);
    if (!img.ok) throw new Error(`image ${img.status}`);
    const ext = info.mime === 'image/png' ? 'png' : 'jpg';
    const file = `${id}.${ext}`;
    writeFileSync(new URL(file, outDir), Buffer.from(await img.arrayBuffer()));

    photos[id] = `img/poi/${file}`;
    credits.push(`- \`${file}\` — [${hit.title}](${info.descriptionurl}) (Wikimedia Commons; see the file page for author & license)`);
    console.log(`ok   ${id}  ←  ${hit.title}`);
    await sleep(600);
  } catch (err) {
    console.log(`skip ${id}: ${err.message}`);
  }
}

writeFileSync(
  photosPath,
  `// AUTO-GENERATED by scripts/fetch-photos.mjs — do not edit by hand.
// POI/hotel id → bundled photo path (relative to the app base URL).
export const PHOTOS: Record<string, string> = ${JSON.stringify(photos, null, 2)};
`,
);
if (credits.length) appendFileSync(new URL('../PHOTO_CREDITS.md', import.meta.url), credits.join('\n') + '\n');
console.log(`\ntotal photos now: ${Object.keys(photos).length}`);
